#!/usr/bin/env node
/**
 * Generates src/main/build-config.ts from build-time env vars.
 *
 * Why a codegen step?
 *   tsc and the bin/ai-yard.js launcher don't embed runtime env vars into the
 *   packaged Electron artifact. Without baking values at build time, the
 *   release.yml "SENTRY_DSN: ${{ secrets.SENTRY_DSN }}" block only sets the
 *   *build shell* env, not the packaged app's runtime env — so opt-in crash
 *   reports / telemetry stay inert in shipped builds.
 *
 *   This script reads env vars at build time and emits a TS module with their
 *   values hardcoded. When an env var is unset (e.g. local dev `npm run build`),
 *   the getter falls back to process.env at call time so dev launches still
 *   work.
 *
 * Inputs (env vars):
 *   SENTRY_DSN              — Sentry DSN for crash reports
 *   TELEMETRY_ENDPOINT      — Umami /api/send URL
 *   TELEMETRY_WEBSITE_ID    — Umami website UUID
 *
 * Output:
 *   src/main/build-config.ts (gitignored, regenerated on every build)
 */
const fs = require('fs');
const path = require('path');

const target = path.resolve(__dirname, '..', 'src', 'main', 'build-config.ts');

function tsLiteral(value) {
  if (typeof value !== 'string' || value.length === 0) return 'undefined';
  return JSON.stringify(value);
}

const SENTRY_DSN = process.env.SENTRY_DSN || '';
const TELEMETRY_ENDPOINT = process.env.TELEMETRY_ENDPOINT || '';
const TELEMETRY_WEBSITE_ID = process.env.TELEMETRY_WEBSITE_ID || '';

const content = `// AUTO-GENERATED by scripts/gen-build-config.js — DO NOT EDIT.
// Values are baked at build time when the corresponding env vars are set
// (typically in CI release.yml). When unset, getters fall back to
// process.env at call time so dev/test workflows still work.

const BAKED_SENTRY_DSN: string | undefined = ${tsLiteral(SENTRY_DSN)};
const BAKED_TELEMETRY_ENDPOINT: string | undefined = ${tsLiteral(TELEMETRY_ENDPOINT)};
const BAKED_TELEMETRY_WEBSITE_ID: string | undefined = ${tsLiteral(TELEMETRY_WEBSITE_ID)};

export function getSentryDsn(): string | undefined {
  return BAKED_SENTRY_DSN ?? process.env.SENTRY_DSN;
}

export function getTelemetryEndpoint(): string | undefined {
  return BAKED_TELEMETRY_ENDPOINT ?? process.env.TELEMETRY_ENDPOINT;
}

export function getTelemetryWebsiteId(): string | undefined {
  return BAKED_TELEMETRY_WEBSITE_ID ?? process.env.TELEMETRY_WEBSITE_ID;
}
`;

fs.writeFileSync(target, content);

const summary = [
  `sentry=${SENTRY_DSN ? 'baked' : 'env-fallback'}`,
  `telemetryEndpoint=${TELEMETRY_ENDPOINT ? 'baked' : 'env-fallback'}`,
  `telemetryWebsite=${TELEMETRY_WEBSITE_ID ? 'baked' : 'env-fallback'}`,
].join(', ');
console.log(`gen-build-config: wrote ${path.relative(process.cwd(), target)} (${summary})`);
